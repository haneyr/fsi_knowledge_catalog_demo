CREATE OR REPLACE TABLE `${project_id}.fsi_bronze.bronze_accounts` AS
SELECT
  CONCAT('ACCT-', LPAD(CAST(n AS STRING), 10, '0')) AS account_id,
  CONCAT('CUST-', LPAD(CAST(CAST(FLOOR(n / 3.0) + 1 AS INT64) AS STRING), 8, '0')) AS customer_id,
  CASE MOD(n, 8)
    WHEN 0 THEN 'CHECKING' WHEN 1 THEN 'SAVINGS' WHEN 2 THEN 'MONEY_MARKET'
    WHEN 3 THEN 'CD' WHEN 4 THEN 'CHECKING' WHEN 5 THEN 'SAVINGS'
    WHEN 6 THEN 'IRA' ELSE 'CHECKING'
  END AS account_type,
  CASE MOD(n, 4)
    WHEN 0 THEN 'Active' WHEN 1 THEN 'Active' WHEN 2 THEN 'Active' ELSE 'Dormant'
  END AS account_status,
  ROUND(CASE MOD(n, 8)
    WHEN 0 THEN 1000 + RAND() * 50000
    WHEN 1 THEN 5000 + RAND() * 100000
    WHEN 2 THEN 10000 + RAND() * 250000
    WHEN 3 THEN 5000 + RAND() * 500000
    WHEN 6 THEN 10000 + RAND() * 1000000
    ELSE 500 + RAND() * 25000
  END, 2) AS current_balance,
  ROUND(CASE MOD(n, 8)
    WHEN 0 THEN 500 + RAND() * 45000
    WHEN 1 THEN 3000 + RAND() * 95000
    ELSE 200 + RAND() * 20000
  END, 2) AS available_balance,
  ROUND(CASE MOD(n, 8)
    WHEN 0 THEN 0.01 WHEN 1 THEN 0.03 + RAND() * 0.02
    WHEN 2 THEN 0.04 + RAND() * 0.01 WHEN 3 THEN 0.04 + RAND() * 0.015
    WHEN 6 THEN 0.0 ELSE 0.005
  END, 4) AS interest_rate,
  CONCAT('0', CAST(10000000 + MOD(n * 17, 90000000) AS STRING)) AS routing_number,
  DATE_ADD('2005-01-01', INTERVAL CAST(FLOOR(RAND() * 7000) AS INT64) DAY) AS open_date,
  CASE WHEN MOD(n, 25) = 0 THEN DATE_ADD('2023-01-01', INTERVAL CAST(FLOOR(RAND() * 700) AS INT64) DAY) ELSE NULL END AS close_date,
  CONCAT('BR-', LPAD(CAST(MOD(n, 500) + 1 AS STRING), 4, '0')) AS branch_id,
  CASE MOD(n, 3) WHEN 0 THEN 'USD' WHEN 1 THEN 'USD' ELSE 'USD' END AS currency_code,
  CASE MOD(n, 8) WHEN 3 THEN CAST(MOD(n, 60) + 3 AS INT64) ELSE NULL END AS term_months,
  CASE MOD(n, 8) WHEN 3 THEN DATE_ADD(DATE_ADD('2023-01-01', INTERVAL CAST(FLOOR(RAND() * 700) AS INT64) DAY), INTERVAL CAST(MOD(n, 60) + 3 AS INT64) MONTH) ELSE NULL END AS maturity_date,
  CASE WHEN MOD(n, 20) = 0 THEN TRUE ELSE FALSE END AS joint_account,
  CASE MOD(n, 5) WHEN 0 THEN 'Online' WHEN 1 THEN 'Branch' WHEN 2 THEN 'Referral' WHEN 3 THEN 'Phone' ELSE 'Online' END AS origination_channel,
  TIMESTAMP_SUB(CURRENT_TIMESTAMP(), INTERVAL CAST(FLOOR(RAND() * 500 * 24 * 60) AS INT64) MINUTE) AS created_at,
  'ATLAS' AS source_system
FROM UNNEST(GENERATE_ARRAY(1, 50000)) AS n
